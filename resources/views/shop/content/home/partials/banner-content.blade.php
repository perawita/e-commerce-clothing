{{-- Ukuran Optimal Gambar untuk Background
Untuk mencegah gambar pecah atau kehilangan kualitas:

Resolusi Gambar yang Optimal:

Karena kontainer memiliki max-w-screen-xl dan max-h-screen-xl, yang berarti sekitar 1280px x 720px 
pada layar besar, ukuran gambar minimal yang disarankan adalah:
Lebar: 1280px
Tinggi: 720px
Namun, untuk layar retina atau layar dengan resolusi lebih tinggi (2x pixel density), sebaiknya gunakan 
gambar dengan resolusi dua kali lipat:
Lebar: 2560px
Tinggi: 1440px
Format Gambar:

JPEG atau WEBP biasanya lebih baik untuk gambar dengan detail tinggi karena ukuran file yang lebih kecil 
tanpa mengorbankan terlalu banyak kualitas.
Pastikan gambar tidak di-compress terlalu tinggi untuk menghindari penurunan kualitas visual.
Pertimbangan Rasio Aspek:

Pastikan gambar memiliki rasio aspek yang sesuai dengan 1280:720 (16:9) untuk mencegah gambar terdistorsi 
atau terpotong saat diatur sebagai latar belakang dengan bg-contain. --}}
